public class SortingUtils {

    public static void printArray(int[] arr) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i]+" \t");
        }
        System.out.println("");
    }

    public static void printArray(char[] arr) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i]+" \t");
        }
        System.out.println("");
    }
}
